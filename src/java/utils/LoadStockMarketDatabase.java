    public void readFile(String originalPath) throws FileNotFoundException, IOException {
        try (BufferedReader br = new BufferedReader(new FileReader(originalPath))) {
            String line = br.readLine();
            while (line != null) {
                listOriginalInstances.add(line);
                line = br.readLine();
            }

        }
    }

    public void txtToSql(String originalPath, PrintWriter writer) throws IOException {
        listOriginalInstances = new ArrayList<>();
        readFile(originalPath);

        String headerSql = "INSERT INTO instance(company_id, instance_open, instance_high,"
                + " instance_low, instance_close, instance_volume, instance_time)"
                + "VALUES (";

        for (int i = 0; i < listOriginalInstances.size(); i++) {
            String[] arrayInstance = listOriginalInstances.get(i).split(",");

            int companyIndex = listCompanyNames.indexOf(arrayInstance[1]);

            if (companyIndex != -1) {
                String observationSql = listCompanyIds.get(companyIndex) + " , " + arrayInstance[2] + " , "
                        + arrayInstance[3] + " , " + arrayInstance[4] + " , " + arrayInstance[5]
                        + " , " + arrayInstance[6] + " , "
                        + "to_timestamp('" + arrayInstance[0] + "', 'yyyymmdd'));";

                //System.out.println(headerSql + observationSql);
                writer.println(headerSql + observationSql);
            } else {
                System.out.println(arrayInstance[1]);
            }
        }

    }

